package org.vesuviano.dieti_estates.dto;

public class PreferitoDTO {
	
	int codSezionePreferito;
	int codCliente;
	int codImmobile;
	
	public int getCodSezionePreferito() {
		return codSezionePreferito;
	}
	public void setCodSezionePreferito(int codSezionePreferito) {
		this.codSezionePreferito = codSezionePreferito;
	}
	
	public int getCodCliente() {
		return codCliente;
	}
	public void setCodCliente(int codCliente) {
		this.codCliente = codCliente;
	}
	
	public int getCodImmobile() {
		return codImmobile;
	}
	public void setCodImmobile(int codImmobile) {
		this.codImmobile = codImmobile;
	}
	
}

